import React from 'react';
import { useAtom } from 'jotai';
import { Row, Col, Container } from 'react-bootstrap';
import PageHeader from '../components/PageHeader';
import { favouritesAtom } from '../store';
import BookCard from '../components/BookCard';

export default function Favourites() {
  const [favouritesList] = useAtom(favouritesAtom);

  if (favouritesList === undefined) return null;

  if (favouritesList.length === 0) {
    return (
      <>
        <PageHeader text="Nothing Here" subtext="Add a book to your favourites list." />
      </>
    );
  }

  return (
    <Container>
      <PageHeader text="Favourites" subtext="Your Favourite Books" />
      <Row className="gy-4">
        {favouritesList.map((workId) => (
          <Col lg={3} md={6} key={workId}>
            <BookCard workId={workId} />
          </Col>
        ))}
      </Row>
    </Container>
  );
}
